export type Translation = (typeof translations)[Lang]

const MONTHS_EN = [
  "January", "February", "March", "April", "May", "June",
  "July", "August", "September", "October", "November", "December",
]
const MONTHS_DE = [
  "Januar", "Februar", "März", "April", "Mai", "Juni",
  "Juli", "August", "September", "Oktober", "November", "Dezember",
]

export function formatMatchDate(dateStr: string, lang: Lang): string {
  const d = new Date(dateStr + "T00:00:00")
  const day = d.getDate()
  const month = lang === "en" ? MONTHS_EN[d.getMonth()] : MONTHS_DE[d.getMonth()]
  const year = d.getFullYear()
  return lang === "en" ? `${day} ${month} ${year}` : `${day}. ${month} ${year}`
}

export function matchLabel(match: Match, lang: Lang): string {
  const home = lang === "en" ? match.home_team_en : match.home_team_de
  const away = lang === "en" ? match.away_team_en : match.away_team_de
  const vs = lang === "en" ? "vs" : "gegen"
  return `${home} ${vs} ${away} - ${formatMatchDate(match.match_date, lang)}`
}

export function teamNames(match: Match, lang: Lang): { home: string; away: string } {
  return {
    home: lang === "en" ? match.home_team_en : match.home_team_de,
    away: lang === "en" ? match.away_team_en : match.away_team_de,
  }
}
